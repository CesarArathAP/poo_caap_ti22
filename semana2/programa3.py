variable1 = 10 #  variable para alamcenar una cadena de caracteristicas
variable2 = 5 #  variable para cadenas de caracteres
print(variable1 + variable2) #  concatena caracteres e imprime los resultados
print("{} + {} = {}".format(variable1, variable2, variable1+variable2))  #concatena caracteres e imprime con funcion format y realiza la suma y al final el resultado

print("{} - {} = {}".format(variable1, variable2, variable1-variable2))

print("{} = {} * {}".format(variable1* variable2, variable1,variable2))

print("{} / {} = {}".format(variable1, variable2, variable1/variable2))